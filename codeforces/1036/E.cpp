bool within_line(pair<ll, ll> pt, int i) {
    if(pt.first < min(a[i].x, b[i].x) || pt.first > max(a[i].x, b[i].x)) return false;
    if(pt.second < min(a[i].y, b[i].y) || pt.second > max(a[i].y, b[i].y)) return false;
    return true;
}
