struct PT
{
    ll x,y;
    PT() {}
    PT(ll x,ll y):x(x),y(y){}
};
ll cross(PT a,PT b)
{
    return a.x*b.y-a.y*b.x;
}
//intersection point between ab and cd
//assuming unique intersection exists
bool line_line_intersection(PT a,PT b,PT c,PT d,PT &out)
{
    ll a1=a.y-b.y;
    ll b1=b.x-a.x;
    ll c1=cross(a,b);
    ll a2=c.y-d.y;
    ll b2=d.x-c.x;
    ll c2=cross(c,d);
    ll det=a1*b2-a2*b1;
    if(det==0) return 0;
    ll p=b1*c2-b2*c1;
    ll q=c1*a2-a1*c2;
    if(p%det||q%det) return 0;
    out=PT(p/det,q/det);
    return 1;
}
